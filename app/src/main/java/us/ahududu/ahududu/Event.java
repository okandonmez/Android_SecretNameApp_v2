package us.ahududu.ahududu;

public class Event {

     String  price;
     String  category;
     String  title;
     String  time;

    public Event(String price, String category, String title, String time) {
        super();
        this.price = price;
        this.category = category;
        this.title = title;
        this.time = time;
    }
}
